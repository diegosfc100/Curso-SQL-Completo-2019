-- SELECT 
-- a ideia básica do SELECT é selecionar uma ou mais colunas(ou todas) de uma tabela.

-- sintaxe
SELECT coluna1, coluna2
FROM tabela;

-- selecionando todas colunas.
SELECT *
FROM tabela;


-- selecionanando nome e sobrenome de todos clientes cadastrados no sistema.
SELECT firstName, lastName
FROM person.Person;
